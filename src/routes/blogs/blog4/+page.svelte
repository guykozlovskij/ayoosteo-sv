    <h1>What’s the difference between an Osteopath and a Chiropractor?</h1>
    <p>
      If I had a penny for the amount of times I’ve been asked this question,
      I’d definitely be able to afford that car I’ve been eyeing.
    </p>
    <p>
      The main difficulty with answering this question is that there are
      differences in the ways practitioners within both professions treat. Some
      osteopaths practice closer to the chiropractic way and some chiropractors
      practice more like an osteopath might. Without boring you with the history
      and philosophy (which I have been guilty of many many times), <span
        class="article-bold"
        >the main difference is that Chiropractors tend to focus on the spine
        and joints whereas Osteopaths look at the body as a whole; they take a
        broader approach</span
      > (for example, if you are complaining of lower back pain s/he will address
      your lower back alongside with your upper back, hip, knee, ankle, some may
      even address your digestion).
    </p>
    <p>
      Is there a difference in treatment? Yes, but like I said before every
      practitioner is different. Generally chiropractors use manipulations (some
      call it “adjustments”) to help restore joint position and function.
      Manipulations are also utilised by Osteopaths but they also take a broader
      approach so use other techniques like massage, joint mobilisation, etc.
      Both Chiropractors and Osteopaths give exercises and advice regarding your
      lifestyle and the duration of consultation and treatment is typically
      longer with an osteopath but then again, every clinic and practitioner is
      different.
    </p>
    <p>
      Do they treat different conditions? They both treat all types of muscle,
      joint and ligament injuries, neck and back pain, headaches etc.
    </p>
    <p>How about the cost? Very similar.</p>
    <p>
      So there are more similarities than differences. What is important is YOU.
      As long as:
    </p>
    <ul class="article-unordered-list">
      <li>
        You have a consultation with the practitioner where you have the freedom
        to express yourself, you feel listened to and understood
      </li>
      <li>
        An effective case history, clinical examination and diagnosis is carried
        out by the practitioner
      </li>
      <li>The diagnosis and course of action is explained to you</li>
      <li>You feel comfortable with the practitioner</li>
      <li>You are receiving effective treatment with exercise and advice</li>
    </ul>
    <p>
      ...Then you have found the right practitioner and the right clinic
      regardless of whether it’s an osteopath or a chiropractor.
    </p>
    <p>I hope this helps.</p>
