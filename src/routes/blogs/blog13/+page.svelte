    <h1>World Obesity Day</h1>
    <p>
      Today is March 4th — <strong>World Obesity Day</strong> — a day of action calling
      for a response to the obesity crisis.
    </p>
    <p>
      If you’re a morning news reader like me,  I am sure you have seen
      headlines like <a
        href="https://www.aljazeera.com/news/2025/3/4/most-adults-a-third-of-children-will-be-overweight-or-obese-by-2050-study"
        style="color: rgb(0, 105, 69)"
        target="_blank"
        >“Most adults and a third of children will be overweight or obese by
        2050”</a
      >
      or
      <a
        href="https://www.bbc.co.uk/news/articles/cy87d2g81yxo"
        style="color: rgb(0, 105, 69)"
        target="_blank"
        >“Over 50% of adults worldwide predicted to be obese or overweight by
        2050.”</a
      >
    </p>
    <p>
      These articles are based on global data published in The Lancet journal.
      It is a fascinating read, and I will link both the <a
        href="https://www.thelancet.com/journals/lancet/article/PIIS0140-6736(25)00397-6/fulltext?dgcid=tlcom_carousel2_whod_gbd25_lancet"
        style="color: rgb(0, 105, 69)"
        target="_blank">full article</a
      >
      and a shorter
      <a
        href="https://www.thelancet.com/journals/lanpub/article/PIIS2468-2667(25)00049-0/fulltext?dgcid=tlcom_carousel1_whod_eds25_lanpub"
        style="color: rgb(0, 105, 69)"
        target="_blank">editorial</a
      > below.
    </p>
    <p>
      The paper highlights how numerous studies have shown a relentless increase
      in obesity rates among children, teenagers, and adults—along with the
      disease burden this will place on governments and healthcare systems.
      Conditions like type 2 diabetes, coronary heart disease, stroke,
      hypertension, and cancer are all linked to obesity.
    </p>
    <p>
      I completely agree with the journal’s call to action:
      <br />✅ Governments need to implement laws, regulations, and policies.
      <br />✅ Schools and communities should promote healthy lifestyles.
      <br />✅ Food manufacturers and supermarkets must take responsibility.
    </p>
    <p>But we also need to talk about the individual.</p>
    <p>Education is key.</p>
    <p>
      We should not  wait for governments or food companies to act. Yes, it’s
      ridiculous that healthier food choices often cost more—in the UK, <a
        href="https://foodfoundation.org.uk/press-release/dietary-inequalities-worsened-last-two-years-healthier-foods-grew-more-expensive"
        style="color: rgb(0, 105, 69)"
        target="_blank"
      >
        the Food Foundation
      </a>found that healthy food can be twice as expensive per calorie as
      unhealthy food.
    </p>
    <p>However, there’s still so much you can do as an individual.</p>
    <p>
      📌 A study published in <a
        href="https://bmjopen.bmj.com/content/9/10/e027546"
        style="color: rgb(0, 105, 69)"
        target="_blank">BMJ Open</a
      > found that ultra-processed foods make up 57% of the average UK adult’s diet—and
      an even more shocking 65% for children.
    </p>
    <p>
      There’s a growing body of evidence linking ultra-processed foods to a
      higher risk of obesity, dental disease, high blood pressure, type 2
      diabetes, cardiovascular disease, anxiety, and depression.  So reducing
      our consumption of ultra processed food is important for health and
      longevity.
    </p>
    <p>But let’s be honest—the system is stacked against you.</p>
    <p>
      From the cost of healthy food to misleading packaging and aggressive
      marketing of ultra-processed products, it is no surprise that so many
      people struggle with weight and nutrition.
    </p>
    <p>
      If you’ve tried making changes but feel stuck, you are not alone. As a
      health coach, my goal is to help you break free from these challenges and
      take back control of your health—without fad diets or extreme
      restrictions.
    </p>
    <p>
      Now, I’m not saying you should completely eliminate ultra-processed foods
      from your diet—because, let’s be honest, for most of us, that’s just not
      realistic. But we can aim to eat a lot less of them.
    </p>
    <h2>A few tips</h2>
    <br />
    <p>We can all take small steps to improve our diets:</p>
    <ul style="list-style-type: none" class="article-ordered-list">
      <li>➡️ Drink fewer energy drinks</li>
      <li>
        ➡️ Swap mass-produced white bread for sourdough, rye, or granary bread.
      </li>
      <li>➡️ Snack on nuts instead of crisps</li>
      <li>➡️ Replace sugary cereals with oats or eggs for your kids.</li>
      <li>
        ➡️ Swap store-bought salad dressings for a simple homemade olive oil and
        vinegar dressing
      </li>
    </ul>
    <p>Start small. Make it manageable.</p>
    <p>Learn to read food labels.</p>
    <p>
      Taking a closer look at ingredient labels can help you make better
      choices, find healthier alternatives, and even save money.
    </p>
    <p>
      <strong>My point is this:</strong> Our response to this issue should not stop
      at calling for change from policymakers. We also need to look at ourselves.
    </p>
    <p>Ask yourself:</p>
    <ul style="list-style-type: none" class="article-ordered-list">
      <li>❓ What am I buying and eating?</li>
      <li>❓ What am I feeding my child?</li>
      <li>❓ What small change can I make to improve my health?</li>
    </ul>
    <p>
      By being more mindful of our food choices, we empower ourselves to make
      healthier, more informed decisions. The goal isn’t drastic change
      overnight—it’s about starting small and building sustainable habits that,
      over time, become second nature.
    </p>
    <p>
      Better choices lead to better health and well-being. Let’s start today.
    </p>
  </div>
