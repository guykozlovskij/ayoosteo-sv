    <h1>Adjustment: What causes that pop or crack noise?</h1>
    <p>
      After the facial and/or audible reaction to “pop”/”crack” sound of an
      adjustment, I’m almost always asked the question “What causes that
      pop/crack noise” and “is it safe?”
    </p>
    <p>
      Well don’t worry, the sound is not actually your spine or joint “popping”
      or “cracking”. The actual pop/crack is called a <span class="article-bold"
        >cavitation.</span
      > The result of the noise is quite similar to the release of gas bubbles when
      you cork a champagne bottle (I would say that the effect is also very similar)It’s
      basically the release of gas (nitrogen) that causes the popping sound.
    </p>
    <p>
      Around many of your joints are capsules, which contain a fluid (synovial
      fluid) with dissolved gases. During an adjustment the joints are slightly
      separated, creating a vacuum within the joint capsule. The sound is then
      created by nitrogen gas rushing in to the partial vacuum created.
    </p>
    <p>
      But it is important to know that the absence of the noise does not mean
      that the adjustment was unsuccessful – it only means that there was no
      noise from the escaping gas.
    </p>
    <p>
      <span class="article-bold">Why do you adjust the spine?</span> To relieve pain
      and improve physical function.
    </p>
    <p>
      <span class="article-bold">Is it safe?</span> When performed by a trained and
      licenced practitioner… Absolutely.
    </p>
    <p>
      <span class="article-bold">Are there any side effects?</span> Sometimes a slight
      soreness and discomfort in the treated area but goes away within a day or two.
      Serious complications are very very rare.
    </p>
    <p>
      Any possible side effect should be explained to you by your practitioner
      and consent should be obtained from you before performing an adjustment on
      you. If you are still uncomfortable with adjustment – not to worry – there
      are many other techniques your practitioner can do to relieve your pain
      and improve physical function.
    </p>
    <p>I hope this helps.</p>
