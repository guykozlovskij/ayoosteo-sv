    <h1>Principles Behind Osteopathic Treatment</h1>
    <p>
      Osteopathic treatment is not merely about the techniques; it is a
      philosophy of healthcare based on guiding principles. Osteopathy is a vast
      art and science that is open to many interpretations but there are a set
      of core principles that expresses our distinctiveness as Osteopaths.
    </p>
    <p>
      This post is a summary of <span
        >Stephen Paulus’s article in the Internal Journal of Osteopathic
        Medicine.</span
      ><span class="article-bold"
        >These core principles are the basis of osteopathic assessment and
        treatment.
      </span>
    </p>
    <p>Now that I have your attention, here are 5 core principles:</p>
    <p>
      1. Human beings function holistically “We look at the body in health as
      meaning perfection and harmony, not in one part, but as the whole”
    </p>
    <p>
      2. The body has the ability to heal itself or adapt, in the presence of
      disease or injury
    </p>
    <p class="article-bold">
      If we as Osteopaths can remove the obstructions that cause disease and
      support the conditions that promote healing, then nature’s chemistry, drug
      store, and remedies can perform the necessary actions to restore
      equilibrium at whatever level is possible for that individual patient.
    </p>
    <p>3. Structure (anatomy) and function (physiology) are interrelated</p>
    <p class="article-bold">
      From a clinical perspective, we utilise the interrelationship between
      structure and function to inform Osteopathic Manipulative Treatment.
    </p>
    <p>4. Palpation is essential</p>
    <p class="article-bold">
      The science of Osteopathy cannot exist without knowing and understanding
      the intricacies of anatomy.
    </p>
    <p>
      5. The musculoskeletal system (bones, muscles, and connective tissues)
      impacts the overall health of an individual
    </p>
    <p class="article-bold">
      Bones as well as muscles and connective tissues form a mechanical
      scaffolding and provide an essential portal, or handle, allowing access to
      other tissues and body functions.
    </p>
    <p>
      These core principles also make osteopathy distinct from physiotherapy,
      chiropractic and other manual therapies.
