      <p>How does it work?</p>
    </strong>
    <p>
      “Acupuncture stimulates the nerves in skin and muscle, and can produce a
      variety of effects. We know that it increases the body’s release of
      natural painkillers – endorphin and serotonin – in the pain pathways of
      both the spinal cord and the brain. This modifies the way pain signals are
      received.” (The British Medical Acupuncture Society)
    </p>
    <strong>
      <p>How can Western Medical Acupuncture help you?</p>
    </strong>
    <p>
      Currently the National Institute of Health and Care Excellence (NICE)
      recommends acupuncture for chronic tension type headaches and migraines.
      It is also used to treat neck pain, back pain, muscular or ligamentous
      strain, arthritis, joint pain, tendonitis, and chronic pain.
    </p>
    <strong>
      <p>Is it safe?</p>
    </strong>
    <p>
      Western medical acupuncture is regarded as being completely safe when
      practiced by a qualified health practitioner who has had extra training in
      acupuncture, as there are anatomical risks associated with needling
      specific sites.
    </p>
    <p>
      Sterile needles are used and are safely disposed of after use. The
      practice and practitioner are registered with the City Council to carry
      out acupuncture; I am fully insured and a member of the British Medical
      Acupuncture Society.
    </p>
    <strong>
      <p>Does it hurt?</p>
    </strong>
    <p>
      Acupuncture should not be painful as the needles are much finer than the
      needles used for injections and blood tests. There is usually a pin prick
      sensation on piercing the skin, and subsequent sensations are mild for
      most people during a normal manual acupuncture session. Most of the times,
      once the needle is in, it’s hardly noticeable.
    </p>
    <strong>
      <p>Are there any side effects?</p>
    </strong>
    <p>
      Here is a list of some of the side effects you may, or may not,
      experience:
    </p>
    <ul>
      <li>Momentary discomfort where the needles are inserted</li>
      <li>Drowsiness and sleepiness</li>
      <li>Bruising</li>
      <li>Temporary worsening of your symptoms</li>
      <li>Fainting</li>
      <li>Onset of a migraine headache, if you are a sufferer</li>
      <li>Feeling faint, during or after treatment.</li>
    </ul>
    <p>
      Here is a list of some of the rare, but serious, problems that may be a
      risk:
    </p>
    <ul>
      <li>Damage to an internal organ from insertion of a needle</li>
      <li>Infection in the area where the needle was inserted</li>
      <li>Infection from Hepatitis or HIV if dirty needles are used</li>
      <li>
        Infection spreading through the blood system to infect previously
        damaged heart valves.
      </li>
    </ul>
    <p>Please remember that, luckily, the above problems are very rare.</p>
    <p>
      Acupuncture should be used with due caution in pregnant women, although
      there are no established risks that are specific to pregnancy.” (The
      British Medical Acupuncture Society)
    </p>
