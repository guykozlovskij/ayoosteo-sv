    <h1>What is Osteopathy?</h1>
    <p>
      Osteopathy is a health care system based upon finding and treating the
      cause of a disease or injury rather than only attempting to address
      symptoms. Pain is a symptom, not a disease.So addressing the origin of
      what is causing the pain is always the goal of osteopathic treatment.
      Sometimes pain overlaps with the cause so when we treat the pain we treat
      the cause but most times pain is distant from the cause so we find the
      distant cause and address it either by osteopathic treatment or by
      referral to an appropriate medical professional.<br />
      Osteopaths utilise a patient specific, anatomically accurate hands-one procedure
      known as Osteopathic Manipulative Treatment, which can include:High velocity
      low amplitude, muscle energy, counterstrain, myofascial release, craniosacral,
      and lymphatic drainage. Many Osteopaths also utilise other techniques like
      massage, acupuncture, hydrotherapy, taping, exercise therapy etc. (We’ll talk
      about these techniques more specifically in many other post).
    </p>
    <p>
      Irregardless of the technique used the goal of an Osteopath is always to
      restore the body’s natural ability to self-heal or to creatively
      compensate by strengthening the local and global heath of the body and by
      removing obstructions to proper blood flow, fluid flow or nerve function.
      During an osteopathic intervention,
    </p>
    <ol class="article-ordered-list">
      <li>
        First we make a through anatomy diagnosis while monitoring the vitality
        of the tissue.
      </li>
      <li>
        Second we apply a tissue specific and patient specific osteopathic
        manipulative treatment. Treatment will not be only local to the primary
        site of complaint but would also be above and below the site of
        complaint, for example, for shoulder pain, expect the osteopath to apply
        hands-on treatment not only to your shoulder but also to your neck,
        upper and lower back and elbow.
      </li>
      <li>
        Finally, we let the body do the true work of healing of repair,
        renovation, restoration of motion and creative compensation.
      </li>
    </ol>
