    <h1>Do Osteopaths treat headaches?</h1>
    <p>I had this question from a patient recently and the answer is YES.</p>
    <p>
      But it depends on the cause of your headaches, as headaches can be primary
      or secondary to a separate condition.<br />
      Generally the types of headache osteopaths treat are the ones related to your
      muscles and spinal posture for example tension headaches.
    </p>
    <p>
      Your first appointment with the osteopath will involve a lot of questions
      â€“ you will be asked questions like: when did your headaches begin, what
      aggravates and relieves your headaches etc.As osteopaths, questions are
      our best diagnostic tool, so you may think some of the questions are
      irrelevant but actually they help us to rule out a more serious underlying
      cause, which may call for referral to your GP or a specialist.
    </p>
    <p>
      Your osteopaths will then carry out some tests and examination, s/he will
      assess the movement along your neck, upper back and shoulders, take your
      blood pressure, carry out some neurological tests and so on.
    </p>
    <p>
      After the tests and examinations, treatment will begin if results show
      that your headaches have a musculo-skeletal component and there is no red
      flag (basically no worrying symptoms or result from tests and
      examinations).If there are red flags, your osteopath will refer you to
      your GP, a specialist or if necessary to the A&E (very rare).
    </p>
    <p>
      There are many many technique used by osteopaths to treat headaches, which
      does not necessarily include adjustments (as the thought of being adjusted
      scares a lot of people with headaches). Your osteopath will also give you
      some stretches; advice on posture and some may even address your diet.
    </p>
    <p>So go on then! Book an appointment with your local osteopath.</p>
    <p>Hope this helps.</p>
    <p>If you have any questions, feel free to ask below or email.</p>
    <p>Ayo</p>
